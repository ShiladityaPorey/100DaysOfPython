# Original tuple
first_tuple=('a','b', 'c', 'd', 'e', 'f')


# Convert tuple to list
tuple_to_list= list(first_tuple)
print(type(first_tuple))   # <class 'tuple'>
print(type(tuple_to_list)) # <class 'list'>


# Original list
first_list=['a','b', 'c', 'd', 'e', 'f']

# Convert list to tuple
list_to_tuple= tuple(first_list)
print(type(first_list))  # <class 'list'>
print(type(list_to_tuple)) # <class 'tuple'>

